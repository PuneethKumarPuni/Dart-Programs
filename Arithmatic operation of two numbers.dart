void main() {
  var n1 = 10;
  var n2 = 20;

  var sum = n1 + n2;
  print("sum of ${n1} and ${n2} is ${sum}");

  var diff = n2 - n1;
  print("difference of ${n1} and ${n2} is ${diff}");

  var pro = n1 * n2;
  print("product of ${n1} and ${n2} is ${pro}");

  var div = n2 / n1;
  print("division of ${n1} and ${n2} is ${div}");

  var mod = n2 % n1;
  print("reminder of ${n1} and ${n2} is ${mod}");
}
